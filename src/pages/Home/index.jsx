import { useEffect, useState, useRef } from "react";
import "./style.css";
import Trash from "../../assets/trash.svg";
import api from "../../services/api";

function Home() {
  const [users, setUsers] = useState([]);

  const inputName = useRef();
  const inputEmail = useRef();
  const inputAge = useRef();

  async function getUsers() {
    const usersFromApi = await api.get("/usuarios");
    setUsers(usersFromApi.data);
  }

  async function postUsers() {
     await api.post("/usuarios",{
      name: inputName.current.value,
      email: inputEmail.current.value,
      age: Number (inputAge.current.value),
     });
   
    getUsers();
  }

  async function deleteUsers(id) {
   await api.delete(`/usuarios/${id}`);
    
    getUsers();
  }


  useEffect(() => {
    getUsers();
  }, []);



  return (
    <>
      <div className="container">
        <form>
          <h1>Cadasto de Usuarios</h1>
          <input type="text" ref={inputName} placeholder="Nome" />
          <input type="email" ref={inputEmail} placeholder="Email" />
          <input type="number" ref={inputAge} placeholder="Idade" />
          <button type="button"onClick={postUsers}>Cadastrar</button>
        </form>

        {users.map((user) => (
          <div key={user.id} className="card">
            <div>
              <p>
                Nome: <span>{user.name}</span>{" "}
              </p>
              <p>
                Idade: <span>{user.age}</span>{" "}
              </p>
              <p>
                Email: <span>{user.email}</span>{" "}
              </p>
            </div>
            <button onClick={() => deleteUsers(user.id)}>
              <img src={Trash} className="icon" alt="Excluir" />
            </button>
            

          </div>
        ))}
      </div>
    </>
  );
}

export default Home;
